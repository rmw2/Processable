        ## 16hello.s
        ## ============================================================

        ## ============================================================
                                .section ".rodata"
        ## ============================================================
        
pcGreeting: .asciz  "Hello World\n"
        
        ## ============================================================
                                .section ".bss"
        ## ============================================================
        
        ## ============================================================
                                .section ".text"
        ## ============================================================

        ## ------------------------------------------------------------
        ## int main(int argc, char *argv[])
        ##
        ## Write "Hello\n" to stdout.
        ##
        ## Formal parameter offsets:
        ##      .equ ARGC, 8
        ##      .equ ARGV, 12
        ## ------------------------------------------------------------

        .globl  main

main:

        pushl   %ebp
        movl    %esp, %ebp

        ## printf("Hello\n");
        movabsq $pcGreeting, %rdi 
        call    printf
        movl    $4, %eax 
        addl    %eax, %esp 
        
        ## return 0;
        movl    $0, %eax
        movl    %ebp, %esp
        popl    %ebp
        ret

