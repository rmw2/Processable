pcGreeting: .asciz  "Hello World\n"

        ## ============================================================
                                .section ".bss"
        ## ============================================================

        ## ============================================================
                                .section ".text"
        ## ============================================================

        ## ------------------------------------------------------------
        ## int main(int argc, char *argv[])
        ##
        ## Write "Hello\n" to stdout.
        ##
        ## Formal parameter offsets:
        ##      .equ ARGC, 8
        ##      .equ ARGV, 12
        ## ------------------------------------------------------------

        .globl  main

main:   pushq   %rbp
        movq    %rsp, %rbp

        ## printf("Hello\n");
        movabsq $pcGreeting, %rdi
        call    printf
        movabsq $4, %rax
        addq    %rax, %rsp

        ## return 0;
        movl    $0, %eax
        movq    %rbp, %rsp
        popq    %rbp
        ret

